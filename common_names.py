# Normalize lists by adding lowercase versions and versions with 'е' instead of 'ё'
def normalize_name_lists():
    """
    Normalize the name lists by adding lowercase versions and versions with 'е' instead of 'ё'.
    """
    global COMMON_FIRST_NAMES, COMMON_PATRONYMICS
    
    # Process first names
    normalized_first_names = set(COMMON_FIRST_NAMES)
    for name in list(COMMON_FIRST_NAMES):
        normalized_first_names.add(name.lower())
        if 'ё' in name.lower():
            normalized_first_names.add(name.lower().replace('ё', 'е'))
            normalized_first_names.add(name.replace('Ё', 'Е'))
    
    # Process patronymics
    normalized_patronymics = set(COMMON_PATRONYMICS)
    for patronymic in list(COMMON_PATRONYMICS):
        normalized_patronymics.add(patronymic.lower())
        if 'ё' in patronymic.lower():
            normalized_patronymics.add(patronymic.lower().replace('ё', 'е'))
            normalized_patronymics.add(patronymic.replace('Ё', 'Е'))
    
    COMMON_FIRST_NAMES = list(normalized_first_names)
    COMMON_PATRONYMICS = list(normalized_patronymics)

# Normalize the lists when the module is imported
normalize_name_lists() 